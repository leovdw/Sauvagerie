    <p>
      Selon la loi du 11 février 2005, les entreprises de plus de 20 salariés sont  tenu d'employer au moins 6% de travailleurs handicapés pourtant, le taux de chômage chez les handicapés est deux fois supérieur à la normal par rapport au reste de la population.
    </p>
    <p>
      Pourquoi ? <br>
      Les personnes en situation de handicap sont trop souvent victimes de préjugés injustifiés qui font peur aux employeurs. C’est là qu’AAVI, l’Association d’Aide aux Victimes d’Invalides basée à Paris,
      intervient afin de favoriser l’insertion professionnelle des personnes valides nous avons décidé de débarrasser
      la population de ses idées reçus en informant sur la réalitée du handicap en entreprise.
    </p>

